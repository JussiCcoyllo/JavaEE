package be.intecbrussel;

public class ForLoops {
    public static void main(String[] args) {
        //index++
        //++index

        for(int index = 0; index < 10; index++) {
            System.out.println("index++ " + index);

        }
        for(int index = 10; index > 0; index-=2) {
            System.out.println("index-- " + index);
        }


    }
}
